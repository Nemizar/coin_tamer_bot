// Package ports определяет интерфейсы портов для взаимодействия с внешними системами.
// Содержит фабрики для создания единиц работы (Unit of Work).
package ports

import (
	"context"
)

// UnitOfWorkFactory определяет интерфейс для создания экземпляров UnitOfWork.
// Позволяет создавать новые единицы работы с изолированным контекстом.
type UnitOfWorkFactory interface {
	// New создает и возвращает новый экземпляр UnitOfWork.
	// Возвращает ошибку, если не удалось создать единицу работы.
	// Созданная единица работы должна быть корректно освобождена после использования.
	New(ctx context.Context) (UnitOfWork, error)
}
